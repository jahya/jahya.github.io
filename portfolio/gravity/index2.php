						<h1>Gravity</h1>
						<span class="location">@ Jaaga, Bangalore</span>
						<p>The 'Gravity' video and sound installation was created for Jaaga Sound &amp; Lights in 2011.
						It was inspired by Mark Rothko's Seagram Murals, and the work of Barnett Newman.</p>
						<p>It is a work which attempts to create a stationary moment in time. It's slow, 
						continuous self-revelation, the violence of the tear, the relentlessly minute variation - 
						are an attempt to create a deep but direct instinctual connection with the viewer.</p>
						<p>In the work, three canvass panels (8' x 3') are installed in triptych format. They
						are 'painted' with projected light, which cuts through the darkness to create the 
						optical illusion of the tear.</p>
						<p>The soundtrack is continuously self-generating, from a custom-built 
						3-voice software synthesizer. The sound and video then work together to continuously reveal the endless
						variation embedded in their programming.</p>
