						<h1>Untitled (Picture This)</h1>
						<span class="location">@ AlphaVille, London</span>
						<p>This work attempts to highlight the imperceptibility of time, as a concept which
						can only be viewed from the present moment. It was inspired by Molly Pearson's poem
						'Picture This' and displayed at the AlphaVille festival of post-digital culture in 2011.</p>
						<p>The central concept behind this work was to draw out and expose as much as possible from 
						each moment as it passes by:</p>
						<ul class="margin"><li>A microphone listens endlessly to the ticking of a metronome.</li>
						<li>A software program listens intently for audible ticks, and uses them to trigger changes
						in a self-generating visual pattern.</li>
						<li>A mixer feeds the live signal back out through a PA, causing amplification and reverberation 
						as it does so.</li></ul>
						<p>To read more about the creation of this work, and to see Molly Pearson recite her poem
						in front of the installation, see this blog post:<br /> 
						<a href="http://jahya.net/blog/?2011-09-installation-at-alpha-ville">Installation at AlphaVille</a></p>
