						<h1>Whispers</h1>
						<span class="location">@ Macbeth, Hoxton</span>
						<p>This sound installation is designed to accentuate the character of a darkened stairwell 
						in a connecting corridor at the MacBeth, Hoxton. As people pass by, voices can be heard 
						softly whispering unintelligible words at different volumes.</p>
						<p>The sounds begin to trigger as you approach the doorway, but are at a barely audible level.
						By the time you reach the first few steps the whispers have become noticeable, and by the time you
						get to the middle of the stairs they are relentlessly overlapping each other. Once you climb the stairs, the
						whispers die down and disappear.</p>
						<p>The installation is a fun but subtle and simple piece of interaction. For the most-part, people are 
						unaware of the installation until they are experiencing it.</p>
