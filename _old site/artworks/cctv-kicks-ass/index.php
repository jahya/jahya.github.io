						<h1>CCTV Kicks Ass</h1>
						<span class="location">@ Goldsmiths Student Union</span>
						<p>This is an audio-visual installation created for the Goldsmiths Student
						Union building in 2009. The piece is designed to catch the attention of people
						as they enter the building and invite them to stop and interact with it.</p>
						<p>Each of the tiny squares you see projected on the wall are actually captured 
						CCTV camera images. As people pass by the installation, their outlines are captured
						in realtime by a mounted camera. The CCTV 'pixels' are reformed to show the silhouettes
						of the passers-by.</p>
						<p>This is a fun and simple reflection on the nature of omnipresent CCTV - captured CCTV 
						images are used as the palette to build up a larger, real-time, captured CCTV image.
						The movements of viewers are also interpreted in audio form as street noises - cars,
						roadworks etc. - and played back in accordance with changes in their movements.</p> 
						<p>The piece was created in collaboration with <a href="http://www.ricardocarvalho.com">Ricardo Carvalho</a>.</p>
