				<h2>Bio</h2>

				<p>Andrew McWilliams is a British-born, New York-based independent artist, technologist, and program director. Exploring relationships between materiality, sensation and presence, his practice spans installation, sound, light, video and software engineering.</p>

				<p>McWilliams has exhibited work at <a href="http://jaaga.in/">Jaaga Creative Common Ground</a> (India), <a href="http://www.alpha-ville.co.uk/">AlphaVille Festival of Post-Digital Culture</a> (UK), and the <a href="http://www.freemote.nl/">Freemote Co-Creation Festival</a> (Netherlands). He has been awarded residencies at <a href="http://www.cactroy.org/">Contemporary Artist Center</a> (USA), <a href="http://www.i-park.org/">I-Park Foundation</a> (USA), on the <a href="http://jaaga.in/residency/">Jaaga Residency Program</a> (India), and run workshops at the <a href="http://sfpc.io/">School for Poetic Computation</a> (New York).</p>

				<p>McWilliams is founder and director of <a href="http://art-a-hack.github.io/">Art-A-Hack</a>, a biannual co-creation program in New York, and founder and director of <a href="http://hardwarehacklab.io/">Hardware Hack Lab</a>, a weekly arts/technology community workshop. He is also a program coordinator with the <a href="http://nyc.volumetric.org">Volumetric Society</a>, which aims to broaden the conversation between technology and the arts through public presentations and events.</p>

				<h2>Statement</h2>
				<p>I see my work as interventions which undercut the things we take for-granted just by being human. Our daily experience is composed of a continuous, constructed network of mental signs which govern our perception. Using technology as a medium, I create sensory environments which seek to question - and alter - how we navigate, make sense of, and relate to the world.</p>

				<p>The form of these installations can be assemblages of canvass, found objects or materials, combined with projected sound, video or light. My work is time-based but usually non-repeating, employing software engineering techniques to generate behavior on-the-fly.</p>

				<p>These environments are neither representative nor abstract. Instead they are better characterized as experiential interplays between ideas. I am trying to get to an earlier, pre-or-early conscious stage, in which form, color, texture and emotion trigger new ideas prior to attachment to an existing mental network.</p>
